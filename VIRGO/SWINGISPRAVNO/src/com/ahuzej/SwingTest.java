    public void toggleButtons(JButton btnClicked) {
        btnClicked.setEnabled(false);
        for (JButton mpButton : mpButtons) {
            if(!mpButton.isEnabled() && mpButton != btnClicked) {
                mpButton.setEnabled(true);
            }
        }
        
    }
    
    
