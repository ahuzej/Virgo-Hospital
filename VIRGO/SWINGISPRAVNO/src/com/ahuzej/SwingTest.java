    private void btnDeleteMPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteMPActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        Doctor doc = (Doctor)cbDeleteMP.getSelectedItem();
        int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove " + doc.getFirstName() + " " + doc.getLastName(), "Remove", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION) {
            DOCTORS_HANDLER.deleteDoctor(doc.getIDDoctor());
            initPatientList();
            JOptionPane.showMessageDialog(this, "Doctor removed.");
            loadReceptionPatients();
                if(cbDoctors.getItemCount() != 0) {
                int selectedDoctorId = ((Doctor)cbDoctors.getSelectedItem()).getIDDoctor();   
                loadPatientList(lbPatients, selectedDoctorId);
                loadPatientList(lbPatientsBilling, selectedDoctorId);
                
            }
                else
                {
                        DefaultListModel listmodel = (DefaultListModel)lbPatients.getModel();
                        DefaultListModel listmodelBilling = (DefaultListModel)lbPatientsBilling.getModel();
                        listmodel.clear();
                        listmodelBilling.clear();
                    //lbPatients.setModel(null); 
                }
        }
        
    }//GEN-LAST:event_btnDeleteMPActionPerformed

